    private void buttonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSalvarActionPerformed

        boolean validacao = this.validarFormulario();

        if (validacao) {
            String nome = textNome.getText();
            int matricula = Integer.parseInt(textMatricula.getText());

            aluno.setNome(nome);
            aluno.setMatricula(matricula);

            boolean retorno = alunoControl.alterar(aluno);

            if (retorno) {
                CadastroAlunoView.carregarTabela();
                JOptionPane.showMessageDialog(null, "Aluno alterado com sucesso.");
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao tentar alterar o aluno.");
            }
            this.bloquearFormulario();
            this.gerenciarBotoes(false, false, true);
        }
    }//GEN-LAST:event_buttonSalvarActionPerformed

