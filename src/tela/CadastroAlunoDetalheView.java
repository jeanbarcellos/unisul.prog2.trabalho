    public class ComboBoxItem {

        private int id;
        private String value;

        public ComboBoxItem() {
            id = 0;
            value = "";
        }

        public ComboBoxItem(int id, String value) {
            this.id = id;
            this.value = value;
        }

        public int getId() {
            return this.id;
        }

        public String getValue() {
            return this.value;
        }

        public void setID(int id) {
            this.id = id;
        }

        public void setValue(String value) {
            this.value = value;
        }

        @Override
        public String toString() {
            return value;
        }

    }
