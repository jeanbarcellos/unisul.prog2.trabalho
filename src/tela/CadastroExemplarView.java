    private void tableExemplarListaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableExemplarListaMouseClicked
        this.bloquearFormulario();

        int indice = tableExemplarLista.getSelectedRow();
        if (indice >= 0 && indice < this.exemplarTM.getRowCount()) {
            Exemplar temp = exemplarTM.getExemplar(indice);
            textId.setText("" + temp.getId());
            textEdicao.setText(temp.getEdicao());
            textLocalizacao.setText(temp.getLocalizacao());
        }

        this.gerenciarBotoes(true, true, true, false, false);
    }//GEN-LAST:event_tableExemplarListaMouseClicked

