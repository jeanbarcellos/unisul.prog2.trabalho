    private void buttonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSalvarActionPerformed

        boolean validacao = this.validarFormulario();

        if (validacao) {
            String edicao = textEdicao.getText();
            String localizacao = textLocalizacao.getText();
            
            Exemplar exemplar = new Exemplar();
            boolean retorno;

            if (textId.getText().equals("")) { // Cadstrar

                exemplar.setLivro(this.livro);
                exemplar.setEdicao(edicao);
                exemplar.setLocalizacao(localizacao);

                retorno = this.exemplarControl.inserir(exemplar);

                if (retorno) {
                    this.carregarTabela();

                    JOptionPane.showMessageDialog(null, "Exemplar Cadastrado com sucesso.");
                } else {
                    JOptionPane.showMessageDialog(null, "Erro ao Tentar cadastrar Exemplar.");
                }

            } else { // Editar

                exemplar.setId(Integer.parseInt(textId.getText()));
                exemplar.setEdicao(edicao);
                exemplar.setLocalizacao(localizacao);

                retorno = this.exemplarControl.alterar(exemplar);

                if (retorno) {
                    JOptionPane.showMessageDialog(null, "Exemplar alterado com sucesso.");
                } else {
                    JOptionPane.showMessageDialog(null, "Erro ao Tentar alterar informações do exemplar.");
                }
            }
        }

        this.limparFormulario();

        this.bloquearFormulario();

        this.gerenciarBotoes(true, false, false, false, false);
    }//GEN-LAST:event_buttonSalvarActionPerformed

