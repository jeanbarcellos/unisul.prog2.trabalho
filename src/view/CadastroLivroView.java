    private void tableLivroListaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableLivroListaMouseClicked
        this.bloquearFormulario();

        int indice = tableLivroLista.getSelectedRow();
        if (indice >= 0 && indice < this.livroTM.getRowCount()) {
            Livro temp = livroTM.getLivro(indice);
            textId.setText("" + temp.getId());
            textTitulo.setText(temp.getTitulo());
            textAutor.setText(temp.getAutor());
        }

        this.gerenciarBotoes(true, true, true, false, false, true);
    }//GEN-LAST:event_tableLivroListaMouseClicked

