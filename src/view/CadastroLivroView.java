    private void buttonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSalvarActionPerformed

        boolean validacao = this.validarFormulario();

        if (validacao) {
            String titulo = textTitulo.getText();
            String autor = textAutor.getText();
            Livro livro = new Livro();
            boolean retorno;

            if (textId.getText().equals("")) { // Cadstrar

                livro.setTitulo(titulo);
                livro.setAutor(autor);

                retorno = this.livroControl.inserir(livro);

                if (retorno) {
                    this.carregarTabela();

                    JOptionPane.showMessageDialog(null, "Livro Cadastrado com sucesso.");
                } else {
                    JOptionPane.showMessageDialog(null, "Erro ao Tentar cadastrar Livro.");
                }

            } else { // Editar

                livro.setId(Integer.parseInt(textId.getText()));
                livro.setTitulo(titulo);
                livro.setAutor(autor);

                retorno = this.livroControl.alterar(livro);

                if (retorno) {
                    JOptionPane.showMessageDialog(null, "Livro alterado com sucesso.");
                } else {
                    JOptionPane.showMessageDialog(null, "Erro ao Tentar alterar informações do livro.");
                }
            }
        }

        this.limparFormulario();

        this.bloquearFormulario();

        this.gerenciarBotoes(true, false, false, false, false, false);
    }//GEN-LAST:event_buttonSalvarActionPerformed

