    private void buttonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonExcluirActionPerformed
        Object[] options = {"Sim", "Cancelar"};
        int retornoExcluir = JOptionPane.showOptionDialog(null, "Tens certeza que deseja excluir ...?", "Informação", JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, options, options[0]);

        if (retornoExcluir == JOptionPane.YES_OPTION) {

            int idExcluir = Integer.parseInt(textId.getText());

            boolean retorno = this.livroControl.excluir(idExcluir);

            if (retorno) {
                this.carregarTabela();
                JOptionPane.showMessageDialog(null, "Livro excluído com sucesso.");
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao excluír o livro.");
            }

            this.limparFormulario();

            this.bloquearFormulario();
        } else {

        }

        this.gerenciarBotoes(true, false, false, false, false, false);
    }//GEN-LAST:event_buttonExcluirActionPerformed

    private void buttonExemplaresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonExemplaresActionPerformed
        int linha = tableLivroLista.getSelectedRow();
        if (linha < 0) {
            JOptionPane.showMessageDialog(null, "Selecione uma livro para gerenciar seus exemplares.");
        } else {
            int idEditar = (int) tableLivroLista.getValueAt(linha, 0);

            CadastroExemplarView detalheView = new CadastroExemplarView(idEditar);
            Principal.desktop.add(detalheView);
            detalheView.getFont();
            detalheView.setPosicao();
            detalheView.setVisible(true);
        }
    }//GEN-LAST:event_buttonExemplaresActionPerformed


